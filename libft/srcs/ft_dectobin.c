#include "libft.h"

int		ft_dectobin(int dec)
{
	int	bin;
	int	p;

	bin = 0;
	p = 1;
	while (dec)
	{
		bin += (dec & 1) * p;
		dec >>= 1;
		p *= 10;
	}
	return (bin);
}
